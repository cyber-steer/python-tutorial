# multiplication(n): n을 입력받아 구구단 n단 출력
def multiplication(n):
    print("-"*50)
    for i in range(1,10):
        print(f'{n} * {i} = {n*i}')
    print("-"*50)
#------------------------------------------------------------------------------
# sum(a,b) : 두 숫자를 받아 덧셈결과를 반환
def sum(a, b): return a+b
#==============================================================================

