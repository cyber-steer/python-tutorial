#==============================================================================
# 딕셔너리는 key와 value의 한쌍으로 이루어져 있다
# key는 중복이 불가능하며 value의 중복은 허용된다
# 딕셔너리는 순서가 없어 key값으로 접근을 할수 있다
# 딕셔너리의 항목들은 수정이 가능하다
#==============================================================================


#==============================================================================
#                               딕셔너리 항목
#------------------------------------------------------------------------------
# count() : 찾을 값의 갯수를 반환
# index() : 찾을 값의 위치를 반환 못찾으면 에러
#------------------------------------------------------------------------------
t = (3,1,2,3)
print("="*20,"딕셔너리 항목","="*20)
print(f"t : {t}")
print("t.count(3) :",t.count(3),"t.count(4) :",t.count(4))
print("t.index(3) :",t.index(3))
#==============================================================================