# 튜플은 리스트와 같이 여러 값들을 할당할수 있다
# 튜플은 순서가 있고 중복이 없으며 항목 수정이 불가능하다
#==============================================================================

#==============================================================================
#                               튜플 함수
#------------------------------------------------------------------------------
# count() : 찾을 값의 갯수를 반환
# index() : 찾을 값의 위치를 반환 못찾으면 에러
#------------------------------------------------------------------------------
t = (3,1,2,3)
print("="*20,"튜플 함수","="*20)
print(f"t : {t}")
print("t.count(3) :",t.count(3),"t.count(4) :",t.count(4))
print("t.index(3) :",t.index(3))
#==============================================================================