#==============================================================================
#               0~4 출력
#------------------------------------------------------------------------------
# 0부터 시작하여 5미만까지 출력된다
#------------------------------------------------------------------------------
print("="*20,"range(5)","="*20)
for i in range(5):
    print(f'i : {i}')
print("="*50)
#==============================================================================
print()
#==============================================================================
#               10~14 출력
#------------------------------------------------------------------------------
# 10부터 시작하여 15미만까지 출력된다
#------------------------------------------------------------------------------
print("="*20,"range(10,15)","="*20)
for i in range(10,15):
    print(f'i : {i}')
print("="*50)
#==============================================================================
print()
#==============================================================================
#               5~1 출력
#------------------------------------------------------------------------------
# 5부터 시작하여 1까지 출력한다
#------------------------------------------------------------------------------
print("="*20,"range(5,0,-1)","="*20)
for i in range(5,0,-1):
    print(f'i : {i}')
print("="*50)
#==============================================================================

