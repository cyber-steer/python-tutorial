#==============================================================================
#                           리스트 인덱스
#------------------------------------------------------------------------------
# 인덱스는 제일 왼쪽문자부터 0,1,2,3...식으로 부여
# 오른쪽 문자부터 -1, -2, -3 ....으로도 사용 가능
#------------------------------------------------------------------------------
l = [10,'hello',True,[1,2,3],{'key':'value'}]
print("="*20,"리스트 인덱스 접근","="*20)
print(f"l : {l}")
print("-"*50)
print("l[0] :",l[0],"\t\t\tl[-5]",l[-5])
print("l[1] :",l[1],"\t\t\tl[-4]",l[-4])
print("l[2] :",l[2],"\t\t\tl[-3]",l[-3])
print("l[3] :",l[3],"\t\tl[-2]",l[-2])
print("l[4] :",l[4],"\tl[-1]",l[-1])
print("="*50)
#==============================================================================
print()
#==============================================================================
#                           리스트 추가 삭제
#------------------------------------------------------------------------------
# append() : 리스트 맨뒤에 항목을 추가
# extend() : 리스트 뒤에 리스트 추가, 더하기 연산과 같다
# pop() : 리스트 맨뒤에 항목을 제거하면서 그 값을 반환
# remove() : 리스트에서 지정한 첫번째 항목 삭제
# copy() : 리스트의 내용을 새로운 리스트에 복사한다
# clear() : 리스트의 내용을 모두 지운다
#------------------------------------------------------------------------------
l = []
print("="*20,"리스트 항목","="*20)
print("l :",l)
print("-"*50)
l.append(1)
print("l.append(1) :\t\t",l)
l.extend([1,2,3,4])
print("l.extend([1,2,3,4]) :\t",l)
i = l.pop()
print("l.pop() :\t\t",l,"\tresult :",i)
l.remove(1)
print("l.remove(1) :\t\t",l)
print("l.copy() :\t\t result :",l.copy())
l.clear()
print("l.clear() :\t\t",l)
print("="*50)
#==============================================================================
print()
#==============================================================================
#                           리스트 검색
#------------------------------------------------------------------------------
# index() : 지정한 값을 찾아 인덱스 반환, 못찾으면 에러
# count() : 해당 값의 갯수 반환
#------------------------------------------------------------------------------
l = [1,2,3,1,2,3]
print("="*20,"리스트 검색","="*20)
print("l :",l)
print("-"*50)
print(f'l.index(3) : {l.index(3)}')
print(f'l.count(3) : {l.count(3)}\tl.count(4) : {l.count(4)}')
#==============================================================================
print()
#==============================================================================
#                           리스트 순서
#------------------------------------------------------------------------------
# sort() : 리스트의 내용을 정렬
# reverse()
#------------------------------------------------------------------------------
l = [2,4,6,3,5,1]
print("="*20,"리스트 순서","="*20)
print("l :",l)
print("-"*50)
l.sort()
print('l.sort() :\t',l)
l.reverse()
print('l.reverse() :\t',l)
