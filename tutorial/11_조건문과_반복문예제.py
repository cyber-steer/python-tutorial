#==============================================================================
#               for 구구단
#------------------------------------------------------------------------------
# 1*1 ~ 9*9 까지 구구단을 출력하는 예제
#------------------------------------------------------------------------------
print("="*20,"구구단 for 이용","="*20)
for i in range(1,10):
    print(f"------------------------{i}단------------------------")
    for j in range(1,10):
        print("%d * %d = %2d"%(i,j,i*j))
    
print("="*50)
#==============================================================================
print()
#==============================================================================
#               while 구구단
#------------------------------------------------------------------------------
# 1*1 ~ 9*9 까지 구구단을 출력하는 예제
#------------------------------------------------------------------------------
print("="*20,"구구단 while 이용","="*20)
i = 0
while i<9:
    i+=1
    j = 0
    print(f"------------------------{i}단------------------------")
    while j<9:
        j+=1
        print("%d * %d = %2d"%(i,j,i*j))
    
print("="*50)
#==============================================================================
print()
