#               홀수 짝수 판별
#------------------------------------------------------------------------------
# 1~10가지 숫자중 홀수와 짝수를 판별 하여 출력
#------------------------------------------------------------------------------
print("="*20,"홀수 짝수 판별","="*20)
for i in range(1,11):
    if i%2 == 0:
        print(f"{i} : 짝수입니다")
    else:
        print(f"{i} : 홀수입니다")
print("="*50)
